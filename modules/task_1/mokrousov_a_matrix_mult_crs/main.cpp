#include <gtest/gtest.h>

#include <list>

struct dot {
    int i;
    int j;
    double val;

    dot(int _i, int _j, double _val) : i(_i), j(_j), val(_val) {}
    friend bool operator==(const dot& rhs, const dot& lhs) {
        return ((rhs.i == lhs.i) && (rhs.j == lhs.j) && (rhs.val == lhs.val));
    }
    friend bool operator!=(const dot& rhs, const dot& lhs) {
        return !operator==(rhs, lhs);
    }
};

struct Matrix {
    int size;
    int nonZeroC;
    std::list<dot> nonZeroData;

    explicit Matrix(int size) : size(size), nonZeroC(0) {}
    Matrix(int size, const std::list<dot>& vec)
        : size(size), nonZeroC(vec.size()), nonZeroData(vec) {}

    friend bool operator==(const Matrix& rhs, const Matrix& lhs) {
        if ((rhs.nonZeroC != lhs.nonZeroC) || (rhs.size != lhs.size))
            return false;
        auto j = lhs.nonZeroData.begin();
        for (auto i = rhs.nonZeroData.begin(); i != rhs.nonZeroData.end();
             ++i) {
            if (*i != *j) return false;
            ++j;
        }
        return true;
    }
};

Matrix getSeqMult(Matrix a, Matrix b) {
    Matrix c(a.size);
    for (auto dotA : a.nonZeroData) {
        for (auto dotB : b.nonZeroData) {
            bool notFind = true;
            for (auto dotC : c.nonZeroData) {
                if (dotC.i == dotA.i && dotC.j == dotA.j) {
                    dotC.val += dotA.val * dotB.val;
                    notFind = false;
                    break;
                }
            }
            if (notFind) {
                c.nonZeroData.push_back(
                    dot(dotA.i, dotB.j, dotA.val * dotB.val));
                c.nonZeroC++;
            }
        }
    }
    return c;
}

